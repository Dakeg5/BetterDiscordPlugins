module.exports = ((_) => {
  const config = {
    info: {
      name: "AlwaysTyping",
      authors: [
        {
          name: "Ahlawat",
          discord_id: "887483349369765930",
          github_username: "Tharki-God",
        },
      ],
      version: "1.1.0",
      description: "Keep showing typing status on the selected channel.",
      github: "https://github.com/Tharki-God/BetterDiscordPlugins",
      github_raw:
        "https://raw.githubusercontent.com/Tharki-God/BetterDiscordPlugins/master/AlwaysTyping.plugin.js",
    },
    changelog: [
      {
        title: "v0.0.1",
        items: ["Idea in mind"],
      },
      {
        title: "v0.0.5",
        items: ["Base Model"],
      },
      {
        title: "Initial Release v1.0.0",
        items: [
          "This is the initial release of the plugin :)",
          "Another useless plugin, will Strencher ever unban me? .______.",
        ],
      },
    ],
    main: "AlwaysTyping.plugin.js",
  };
  return !window.hasOwnProperty("ZeresPluginLibrary")
    ? class {
        load() {
          BdApi.showConfirmationModal(
            "ZLib Missing",
            `The library plugin (ZeresPluginLibrary) needed for ${config.info.name} is missing. Please click Download Now to install it.`,
            {
              confirmText: "Download Now",
              cancelText: "Cancel",
              onConfirm: () => this.downloadZLib(),
            }
          );
        }
        async downloadZLib() {
          const fs = require("fs");
          const path = require("path");
          const ZLib = await fetch(
            "https://rauenzi.github.io/BDPluginLibrary/release/0PluginLibrary.plugin.js"
          );
          if (!ZLib.ok) return this.errorDownloadZLib();
          const ZLibContent = await ZLib.text();
          try {
            await fs.writeFile(
              path.join(BdApi.Plugins.folder, "0PluginLibrary.plugin.js"),
              ZLibContent,
              (err) => {
                if (err) return this.errorDownloadZLib();
              }
            );
          } catch (err) {
            return this.errorDownloadZLib();
          }
        }
        errorDownloadZLib() {
          const { shell } = require("electron");
          BdApi.showConfirmationModal(
            "Error Downloading",
            [
              `ZeresPluginLibrary download failed. Manually install plugin library from the link below.`,
            ],
            {
              confirmText: "Download",
              cancelText: "Cancel",
              onConfirm: () => {
                shell.openExternal(
                  "https://rauenzi.github.io/BDPluginLibrary/release/0PluginLibrary.plugin.js"
                );
              },
            }
          );
        }
        start() {}
        stop() {}
      }
    : (([Plugin, Library]) => {
        const {
          WebpackModules,
          Logger,
          PluginUpdater,
          Utilities,
          Patcher,
          Settings: { SettingPanel, Switch, Textbox },
          DiscordModules: { SelectedChannelStore, UserTypingStore },
        } = Library;
        const TypingStore = WebpackModules.getByProps(
          "startTyping",
          "stopTyping"
        );
        const defaultSettings = {
          showAllTyping: false,
          customChannels: "",
        };
        return class AlwaysTyping extends Plugin {
          constructor() {
            super();
            this.settings = Utilities.loadData(
              config.info.name,
              "settings",
              defaultSettings
            );
          }
          checkForUpdates() {
            try {
              PluginUpdater.checkForUpdate(
                config.info.name,
                config.info.version,
                config.info.github_raw
              );
            } catch (err) {
              Logger.err("Plugin Updater could not be reached.", err);
            }
          }
          onStart() {
            this.checkForUpdates();
            Patcher.after(UserTypingStore, "isTyping", (_, args, res) => {
              this.type();
              if (this.settings["showAllTyping"]) return true;
            });
          }
          type() {
            const channelIds = [
              ...this.settings["customChannels"].split(","),
              SelectedChannelStore.getCurrentlySelectedChannelId(),
            ].filter((n) => n);
            if (channelIds.length)
              for (const id of channelIds) {
                TypingStore.startTyping(id);
              }
          }
          onStop() {
            Patcher.unpatchAll();
          }
          getSettingsPanel() {
            return SettingPanel.build(
              this.saveSettings.bind(this),
              new Switch(
                "Everyone typing",
                "Show everyone as typing. (Client-side only)",
                this.settings["showAllTyping"],
                (e) => {
                  this.settings["showAllTyping"] = e;
                }
              ),
              new Textbox(
                "Custom channels",
                "IDs of channels in which AlwaysTyping should send typing status, other than the currently selected channel. - Separated by commas.",
                this.settings["customChannels"],
                (e) => {
                  this.settings["customChannels"] = e;
                },
                {
                  placeholder: "xxxxxxxxxxxxxxxxxx",
                }
              )
            );
          }
          saveSettings() {
            Utilities.saveData(config.info.name, "settings", this.settings);
          }
        };
        return plugin(Plugin, Library);
      })(window.ZeresPluginLibrary.buildPlugin(config));
})();
/*@end@*/
